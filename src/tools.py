def add(a:int, b: int) -> int:
    """
    Adds a and b.

    Args:
        a (int): first number
        b (int): second number

    """
    return a + b

def multiply(a: int, b: int) -> int:
    """
    Multiply a and b.

    Args:
        a (int): first int
        b (int): second int
    """
    return a * b

def divide(a: int, b: int) -> float:
    """Divide a and b.

    Args:
        a (int): first int
        b (int): second int
    """
    return a / b
